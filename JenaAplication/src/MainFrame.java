    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        if (!fieldsAreEmpty()){
            JOptionPane.showMessageDialog(this, "Los campos de tema y Patron deben de estar rellenos", "Adverterncia", JOptionPane.WARNING_MESSAGE);
            return ;
        }
        
        List<Status> tweets;
        
        String resultado="";
        try{
            tweets = TwitterWrapper.searchTweets(searchStringField.getText(),(Integer)tamField.getValue());
            RDFManager manager = new RDFManager(searchStringField.getText(), themeField.getText() );
            for (Status tweet: tweets){
                manager.createGraph(tweet);
            }

            save(manager);
            
        }catch(TwitterException e){
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }//GEN-LAST:event_searchButtonActionPerformed
    
    private boolean fieldsAreEmpty(){
        return searchStringField.getText().trim().length() > 0 && 
               themeField.getText().trim().length() > 0;
    }
    
    private void save(RDFManager manager){
        Model model = manager.getModel();
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showSaveDialog(this);
        if(result == JFileChooser.CANCEL_OPTION)return;
        if(result == JFileChooser.APPROVE_OPTION);
        String name = fileChooser.getSelectedFile().getAbsolutePath();
        try{
            FileWriter writer;
            if (turtleRadio.isSelected()){
                writer = new FileWriter(name + ".ttl");
                RDFDataMgr.write(writer, model, RDFFormat.TURTLE);
                writer.close();
            }else if (RDFXMLRadio.isSelected()){
                writer = new FileWriter(name + ".rdf");
                RDFDataMgr.write(writer, model, RDFFormat.RDFXML);
                writer.close();
            }
        }catch(Exception e){
        }
    }
    
