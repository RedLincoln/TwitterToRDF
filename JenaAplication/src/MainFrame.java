    public void save(File file,String res){
        try{
           FileWriter writer = new FileWriter(file + ".txt");
           writer.write(res);
           writer.close();
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
